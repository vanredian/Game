

public class Hero {
    private String paladin = "Paladin";
    private String barbarian = "Barbarian";
    private String dwarf = "Dwarf";

    public String getPaladin() {
        return paladin;
    }

    public void setPaladin(String paladin) {
        this.paladin = paladin;
    }

    public String getBarbarian() {
        return barbarian;
    }

    public void setBarbarian(String barbarian) {
        this.barbarian = barbarian;
    }

    public String getDwarf() {
        return dwarf;
    }

    public void setDwarf(String dwarf) {
        this.dwarf = dwarf;
    }


}

